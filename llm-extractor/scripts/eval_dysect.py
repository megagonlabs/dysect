def setup_gpt():
    with open('~/.cred/openai-key.txt', 'r') as f:
        OPENAI_API_KEY = f.read().strip()
    openai.api_key = OPENAI_API_KEY

    
def generate(prompt, model):
    messages=[{"role": "user", "content": prompt}] 
    response = openai.chat.completions.create(
        model=model,
        max_tokens=6000,
        temperature=0.0,
        messages = messages)

    return response.choices[0].message.content

def read_extraction_files(file_path):
    """Read extraction by line"""
    triples = []
    with open(file_path, 'r', encoding='utf-8') as f:
        try:
            lines = json.load(f)
        except Exception:
            logging.error(f"Failed to parse LLM response: {file_path}")
            lines = []
        
        for line in lines:
            if len(line) == 5:
                triples.append(tuple([line[0], line[2], line[3]]))
            elif len(line) == 3:
                triples.append(tuple([line[0], line[1], line[2]]))
    return triples

def read_triples_tsv(file_path):
    """Read triples from a TSV file. Returns a list of tuples."""
    triples = []
    with open(file_path, 'r', encoding='utf-8') as f:
        for line in f:
            parts = line.strip().split('\t')
            if len(parts) >= 3:
                triples.append(tuple(parts[:3]))
    return triples

def strip_code_block_markers(text):
    """Remove Markdown code block markers from LLM output."""
    # Remove triple backticks and optional language specifier
    text = re.sub(r'^```[a-zA-Z0-9]*\s*', '', text)
    text = re.sub(r'```$', '', text)
    # Remove leading/trailing whitespace
    return text.strip()

def llm_judge(extracted_triples, annotated_triples, model):
    """Send triples to LLM and get JSON with TP, FP, FN lists."""
    prompt = (
        "You are required to annotate the response generated by an AI model for information extraction task. "
        "You are given the human annotated gold triples and the AI model extracted triples. "
        "Identify which one of the AI predicted triples are correct in comparison to the gold triples. "
        "Generate the response in JSON format using the keys below, and provide each key's output as a list of triples: "
        "1. true_pos: correctly_predicted_triples "
        "2. false_pos: incorrectly_predicted_triples "
        "3. false_neg: missed_triples "
        "Rules **MUST FOLLOW**:"
        "* If the entities are partially matching consider them a match"
        "* If the entity types mentioned in the relation section does not match, consider them a correct match"
        "* Consider reverse relations as match e.g. A->is_father_of->B then B->is_son_of->A"
        f"GOLD TRIPLES: {json.dumps(annotated_triples, ensure_ascii=False)} "
        f"AI PREDICTED Triples: {json.dumps(extracted_triples, ensure_ascii=False)} "
        "Output only the JSON."
    )
    response = generate(prompt, model)
    cleaned_response = strip_code_block_markers(response)
    try:
        result = json.loads(cleaned_response)
    except Exception:
        logging.error(f"Failed to parse LLM response: {response}")
        result = None
    return result

def save_result_json(result, out_path):
    """Save LLM result JSON to disk."""
    with open(out_path, 'w', encoding='utf-8') as f:
        json.dump(result, f, indent=2)

def calc_metrics(TP, FP, FN):
    """Calculate precision, recall, F1 from LLM result JSON."""
    precision = TP / (TP + FP) if (TP + FP) > 0 else 0.0
    recall = TP / (TP + FN) if (TP + FN) > 0 else 0.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0.0
    return {'precision': precision, 'recall': recall, 'f1': f1, 'TP': TP, 'FP': FP, 'FN': FN}

def main(extractions_dir, annotated_dir, output_dir, all_extraction_dirs, judge_model='gpt-4.1', max_size=500):
    os.makedirs(output_dir, exist_ok=True)
    print(extractions_dir)

    extraction_files = glob.glob(os.path.join(extractions_dir, '*.txt'))

    metrics_list = []
    count=0
    extraction_count = 0
    label_count = 0

    for ext_file in extraction_files[:max_size]:
        count+=1
        print(count)
        doc_id = os.path.basename(ext_file)
        print(f'{doc_id=}')
        # print(f'{prev_iterations=}')

        all_extraction_files = []
        all_extraction_files.append(f'{extractions_dir}/{doc_id}')
        for prev_dir in all_extraction_dirs:
            all_extraction_files.append(f'{prev_dir}/{doc_id}')
    
        print(f'{all_extraction_files=}')
        
        # extracted_triples = read_triples_tsv(ext_file)
        prev_triples_by_doc = set()
        # for prev_path in prev_iterations: # this is for prev iterations
        for prev_file in all_extraction_files: # this is for both pos and neg extractions
            # prev_file = os.path.join(prev_path, doc_id)
            if os.path.exists(prev_file):
                extracted_triples = read_extraction_files(prev_file)
                prev_triples_by_doc.update(extracted_triples)
            else:
                logging.warning(f"Extraction file or Prev file missing: {prev_file}")
        prev_triples = list(prev_triples_by_doc)

        ann_file = os.path.join(annotated_dir, doc_id)
        if not os.path.exists(ann_file):
            logging.warning(f"Annotated file missing for {doc_id}")
            continue

        # annotated_triples = read_triples_tsv(ann_file)
        annotated_triples = read_extraction_files(ann_file)
        out_path = os.path.join(output_dir, doc_id.replace('.txt', '.json'))
    
        if not os.path.exists(out_path):
            logging.info(f"judge not ran on {doc_id}")
            # result = llm_judge(extracted_triples, annotated_triples, judge_model)
            result = llm_judge(prev_triples, annotated_triples, judge_model)
            save_result_json(result, out_path)
        else:
            with open(out_path, 'r') as f:
                result = json.load(f)

        if not result:
            result = {}
        TP = len(result.get('true_pos', []))
        FP = len(result.get('false_pos', []))
        FN = len(result.get('false_neg', []))
        metrics = calc_metrics(TP, FP, FN)
        metrics['doc_id'] = doc_id.replace('.txt', '')
        metrics_list.append(metrics)

        extraction_count += len(prev_triples)
        label_count += len(annotated_triples)
    
    total_TP = sum(m['TP'] for m in metrics_list)
    total_FP = sum(m['FP'] for m in metrics_list)
    total_FN = sum(m['FN'] for m in metrics_list)
    macro_metrics = calc_metrics(total_TP, total_FP, total_FN)
    with open(extractions_dir+'/metrics.json', 'w', encoding='utf-8') as f:
        json.dump({'macro_precision': macro_metrics['precision'],
                       'macro_recall': macro_metrics['recall'],
                       'macro_f1': macro_metrics['f1']}, f, indent=2)

    # Aggregate metrics
    if metrics_list:
        avg_precision = sum(m['precision'] for m in metrics_list) / len(metrics_list)
        avg_recall = sum(m['recall'] for m in metrics_list) / len(metrics_list)
        avg_f1 = sum(m['f1'] for m in metrics_list) / len(metrics_list)
        print(f"Average Precision: {avg_precision:.4f}")
        print(f"Average Recall: {avg_recall:.4f}")
        print(f"Average F1: {avg_f1:.4f}")
        with open(extractions_dir+'/metrics.json', 'a', encoding='utf-8') as f:
            # json.dump(metrics_list, f, indent=2)
            f.write(',\n')
            json.dump({'average_precision': avg_precision,
                       'average_recall': avg_recall,
                       'average_f1': avg_f1,
                       'avg_extraction_count': extraction_count / len(metrics_list),
                       'avg_label_count': label_count / len(metrics_list),
                       'extraction_count': extraction_count,
                       'label_count': label_count,}, f, indent=2)
    else:
        print("No metrics to aggregate.")

